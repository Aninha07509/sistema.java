// Solicita a opção do usuário
let opcao = 3;

// Avalia a opção
switch (opcao) {
  case 1:
    console.log("Você escolheu agua");
    break;
  case 2:
    console.log("Você escolheu coca-cola");
    break;
  case 3:
    console.log("Você escolheu Suco");
    break;
  default:
    console.log("Opção inválida!");
}